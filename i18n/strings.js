function t(key) {
  return (STRINGS[currentLang] && STRINGS[currentLang][key])
      || (STRINGS.ko && STRINGS.ko[key])
      || key;
}

function setLang(lang) {
  if (STRINGS[lang]) {
    currentLang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }
}
